package com.finalProjectLedZeppelin.auth.model;

/**
 * Enumeration of user roles used for authorization.
 * <p>
 * Defines access levels assigned to users within the application.
 */
public enum UserRole {

    /**
     * Standard user role with basic access permissions.
     */
    USER,

    /**
     * Administrative role with elevated permissions.
     */
    ADMIN
}
